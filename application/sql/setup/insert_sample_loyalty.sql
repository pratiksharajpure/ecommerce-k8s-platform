-- Count total loyalty members
SELECT COUNT(*) AS total_members FROM loyalty_program;

-- Count by tier
SELECT 
    tier,
    COUNT(*) AS member_count,
    ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM loyalty_program), 2) AS percentage
FROM loyalty_program
GROUP BY tier
ORDER BY 
    CASE tier
        WHEN 'Platinum' THEN 1
        WHEN 'Gold' THEN 2
        WHEN 'Silver' THEN 3
        WHEN 'Bronze' THEN 4
    END;

-- Points statistics by tier
SELECT 
    tier,
    COUNT(*) AS members,
    MIN(points_balance) AS min_points,
    MAX(points_balance) AS max_points,
    ROUND(AVG(points_balance), 0) AS avg_points,
    MIN(points_earned_lifetime) AS min_lifetime,
    MAX(points_earned_lifetime) AS max_lifetime,
    ROUND(AVG(points_earned_lifetime), 0) AS avg_lifetime
FROM loyalty_program
GROUP BY tier
ORDER BY 
    CASE tier
        WHEN 'Platinum' THEN 1
        WHEN 'Gold' THEN 2
        WHEN 'Silver' THEN 3
        WHEN 'Bronze' THEN 4
    END;

-- Verify all loyalty members have valid customer_ids
SELECT 
    COUNT(DISTINCT lp.customer_id) AS unique_customers,
    COUNT(*) AS total_loyalty_records
FROM loyalty_program lp;

-- Check for customers in loyalty program
SELECT 
    'Customers with loyalty' AS category,
    COUNT(DISTINCT lp.customer_id) AS count
FROM loyalty_program lp
UNION ALL
SELECT 
    'Total customers' AS category,
    COUNT(*) AS count
FROM customers;

-- Recent activity check
SELECT 
    tier,
    COUNT(*) AS active_members,
    MIN(last_activity_date) AS earliest_activity,
    MAX(last_activity_date) AS latest_activity
FROM loyalty_program
WHERE status = 'active'
GROUP BY tier;

